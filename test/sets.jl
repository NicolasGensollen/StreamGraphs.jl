# Set tests
@test Set(["a","c","d","e"]) - Set(["b","a","e","x"]) == Set(["c","d"])
@test Set(["a","c","d","e"]) - Set(["b","x"]) == Set(["a","c","d","e"])
@test Set(["a","c","d","e"]) - Set{String}([]) == Set(["a","c","d","e"])
@test Set{String}([]) - Set(["a","c","d","e"]) == Set([])
@test Set{String}([]) - Set{String}([]) == Set([])
@test Set(["a","c"]) × Set(["b"]) == Set([("a","b"),("c","b")])
@test Set(["a","c"]) ⊗ Set(["b"]) == Set([("a","b"),("b","c")])
@test Set(["a","c"]) × Set(["b","d"]) == Set([("a","b"),("a","d"),("c","b"),("c","d")])
@test Set(["a","c"]) ⊗ Set(["b","d"]) == Set([("a","b"),("a","d"),("b","c"),("c","d")])
@test Set(["a","c","e"]) × Set(["b","d"]) == Set([("a","b"),("a","d"),("c","b"),("c","d"),("e","b"),("e","d")])
@test Set(["a","c","e"]) ⊗ Set(["b","d"]) == Set([("a","b"),("a","d"),("b","c"),("c","d"),("b","e"),("d","e")])
@test Set(["a","b"]) × Set(["b"]) == Set([("a","b"),("b","b")])
@test Set(["a","b"]) ⊗ Set(["b"]) == Set([("a","b")])
@test Set(["a","b"]) × Set{String}([]) == Set([])
@test Set(["a","b"]) ⊗ Set{String}([]) == Set([])
@test Set{String}([]) × Set(["a","b"]) == Set([])
@test Set{String}([]) ⊗ Set(["a","b"]) == Set([])
@test Set{String}([]) × Set{String}([]) == Set([])
@test Set{String}([]) ⊗ Set{String}([]) == Set([])